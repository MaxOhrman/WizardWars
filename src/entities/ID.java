package entities;

/**
 * The ID Enum will list all game objects
 * of our game.
 *
 * Whenever we create object
 * we will also give it an appropriate id
 * from enum ID. This is needed to identify
 * gameObjects from one another
 *
 */

public enum ID {
    Player(),
    Block(),
    Crate(),
    Bullet(),
    Enemy()


}
